CREATE TABLE IF NOT EXISTS `organization` (
  `ID` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `ORGANIZATION_TYPE` varchar(255) DEFAULT NULL COMMENT '机构类型',
  `ORGANIZATION_LEADER` int(11) DEFAULT NULL COMMENT '负责人',
  `ORGANIZATION_LEADERS` int(11) DEFAULT NULL COMMENT '分管领导',
  `ORGANIZATION_CODE` varchar(255) DEFAULT NULL COMMENT '部门编号',
  `ORGANIZATION_STATUS` varchar(255) DEFAULT NULL COMMENT '部门状态',
  `ORGANIZATION_ID` varchar(225) DEFAULT NULL COMMENT '所属部门',
  `ORGANIZATION_TITLE` varchar(255) DEFAULT NULL COMMENT '部门名称',
  `ORGANIZATION_PARENT_ID` varchar(225) DEFAULT NULL COMMENT '组织机构父ID',
  `DEL` int(1) DEFAULT '0' COMMENT '删除标记',
  `UPDATE_DATE` datetime DEFAULT NULL COMMENT '修改时间',
  `UPDATE_BY` varchar(11) DEFAULT NULL COMMENT '修改人',
  `CREATE_DATE` datetime DEFAULT NULL COMMENT '创建时间',
  `CREATE_BY` varchar(11) DEFAULT NULL COMMENT '创建人',
  PRIMARY KEY (`ID`) USING BTREE
) ENGINE=InnoDB AUTO_INCREMENT=75 DEFAULT CHARSET=utf8 COMMENT='组织机构';
CREATE TABLE IF NOT EXISTS `organization_employee` (
  `ID` int(11) unsigned NOT NULL AUTO_INCREMENT COMMENT '主键',
  `MANAGER_ID` int(11) DEFAULT NULL COMMENT '管理员ID',
  `POST_IDS` varchar(255) DEFAULT NULL COMMENT '岗位',
  `ORGANIZATION_ID` int(11) DEFAULT NULL COMMENT '所属机构部',
  `EMPLOYEE_PHONE` varchar(255) DEFAULT NULL COMMENT '手机号',
  `EMPLOYEE_BIRTH_DATE` datetime DEFAULT NULL COMMENT '出生日期',
  `EMPLOYEE_EDUCATION` varchar(255) DEFAULT NULL COMMENT '员工学历',
  `EMPLOYEE_POLITICS` varchar(255) DEFAULT NULL COMMENT '政治面貌',
  `EMPLOYEE_ROLE_IDS` varchar(255) DEFAULT NULL COMMENT '所属角色',
  `EMPLOYEE_STATUS` varchar(255) DEFAULT NULL COMMENT '员工状态',
  `EMPLOYEE_CODE` varchar(255) DEFAULT NULL COMMENT '员工编号',
  `EMPLOYEE_NICK_NAME` varchar(100) NOT NULL COMMENT '员工昵称',
  `EMPLOYEE_SEX` varchar(1) DEFAULT NULL COMMENT '员工性别',
  `DEL` int(1) DEFAULT '0' COMMENT '删除标记',
  `UPDATE_DATE` datetime DEFAULT NULL COMMENT '修改时间',
  `UPDATE_BY` int(11) DEFAULT NULL COMMENT '修改人',
  `CREATE_DATE` datetime DEFAULT NULL COMMENT '创建时间',
  `CREATE_BY` varchar(11) DEFAULT NULL COMMENT '创建人',
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=5 DEFAULT CHARSET=utf8 COMMENT='员工';
CREATE TABLE IF NOT EXISTS `organization_post` (
  `ID` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `POST_DESC` varchar(255) DEFAULT NULL COMMENT '岗位描述',
  `POST_CODE` varchar(255) DEFAULT NULL COMMENT '岗位编号',
  `POST_NAME` varchar(255) DEFAULT NULL COMMENT '岗位名称',
  `DEL` int(1) DEFAULT '0' COMMENT '删除标记',
  `UPDATE_DATE` datetime DEFAULT NULL COMMENT '修改时间',
  `UPDATE_BY` varchar(11) DEFAULT NULL COMMENT '修改人',
  `CREATE_DATE` datetime DEFAULT NULL COMMENT '创建时间',
  `CREATE_BY` varchar(11) DEFAULT NULL COMMENT '创建人',
  PRIMARY KEY (`ID`) USING BTREE
) ENGINE=InnoDB AUTO_INCREMENT=17 DEFAULT CHARSET=utf8 COMMENT='岗位管理';
